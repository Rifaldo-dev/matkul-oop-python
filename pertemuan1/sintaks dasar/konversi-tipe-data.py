"""
Contoh konversi tipe data di Python
Variabel x memiliki tipe data integer (int)
"""

x = 10  # membuat variabel x dengan nilai 10 bertipe int

"""
Nilai pada x akan dikonversi menjadi string
menggunakan fungsi bawaan Python yaitu str()
"""

y = str(x)  # mengubah nilai x dari int menjadi string

# menampilkan tipe data dari variabel y
print(type(y))  

# output: <class 'str'>